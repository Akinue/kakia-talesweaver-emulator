		public static void Handshake(LobbyConnection conn)
		{
			var packet = new Packet(Op.Handshake);
			packet.PutByte(0x00);
			packet.PutByte(0x02);
			packet.PutUInt(conn.Seed);
			packet.PutInt(0); // IP
			packet.PutCompressedString("Lobby Server");
			conn.Send(packet);
		}

		/// <summary>
		/// Sends 0x02 Packet. Often serves as an Acknowledgment, "Loading Done", or Synchronization barrier.
		/// </summary>
		public static void Ack(LobbyConnection conn, byte b1 = 0, byte b2 = 0, string message = "")
		{
			var packet = new Packet(Op.Acknowledge);
			packet.PutByte(b1);
			packet.PutByte(b2);
			packet.PutLpString(message);
			conn.Send(packet);
		}

		/// <summary>
		/// Sends the sequence of packets required after a Reconnect (0x10).
		/// </summary>
		public static void ReconnectSequence(LobbyConnection conn)
		{
			// 1. Meta Files (0x18)
			// This tells the client which resource files to use.
			conn.Send(GetMetaFilePacket());

			// 2. Security/Pin Info (0x3C)
			// Mimic "None" security type for now
			LoginSecurity(conn, CharLoginSecurityType.None);
			/**
			var sec = new Packet((Op)0x3C);
			sec.PutByte(0x01); // Type: None (01)
			sec.PutByte(0x01);
			sec.PutByte(0x01);
			sec.PutByte(0x00);
			sec.PutByte(0x01);
			// Dummy email/string that client sometimes expects
			sec.PutString("kakia@tales.net", true);
			conn.Send(sec);
			**/

			// 3. Ping/Ack (0x02)
			// Often sent to confirm ready state
			var ack = new Packet(Op.Acknowledge); // 0x02
			ack.PutInt(0);
			conn.Send(ack);
		}

		/// <summary>
		/// Sends 0x3C CharLoginSecurityPacket.
		/// </summary>
		/// <param name="type">1=None, 2=Setup, 3=Request</param>
		public static void LoginSecurity(LobbyConnection conn, CharLoginSecurityType type)
		{
			var packet = new Packet(Op.LoginSecurityResponse); // 0x3C
			packet.PutByte((byte)type);
			conn.Send(packet);
		}

		public static void CheckNameResponse(LobbyConnection conn, string resultMessage)
		{
			// Structure from old emulator: [Op:0x02] [0x58] [0x00] [Len] [String]
			var packet = new Packet((Op)0x02);
			packet.PutByte(0x58);
			packet.PutByte(0x00);
			packet.PutString(resultMessage, false); // false = no null terminator after string bytes
			conn.Send(packet);
		}

		/// <summary>
		/// Sends the character list for the Lobby selection screen (0x6B).
		/// Populates the full visual data for each character.
		/// </summary>
		public static void CharacterSelectList(LobbyConnection conn, List<WorldCharacter> characters)
		{
			// ---------------------------------------------------------
			// PACKET 1: The Preamble / Summary
			// The client expects this specific sequence before the list
			// Old Code: writer.Write((short)0); writer.Write((byte)1); writer.Write((byte)0); writer.Write((byte)characters.Count);
			// ---------------------------------------------------------
			var preamble = new Packet(Op.CharacterSelectListResponse); // 0x6B
			preamble.PutShort(0);
			preamble.PutByte(1);
			preamble.PutByte(0);
			preamble.PutByte((byte)characters.Count);
			conn.Send(preamble);

			// ---------------------------------------------------------
			// PACKET 2: The Actual Data List
			// Only sent if characters exist.
			// ---------------------------------------------------------
			if (characters.Count > 0)
			{
				var listPacket = new Packet(Op.CharacterSelectListResponse); // 0x6B

				listPacket.PutByte(1); // Has Characters flag
				listPacket.PutByte((byte)characters.Count);
				listPacket.PutByte(1); // Index Start (usually 1)

				int idx = 0;
				foreach (var c in characters)
				{
					// Assumes PutLobbyCharacterListEntry handles the serialization of the character struct
					// This corresponds to the Old Code's Character.ToBytes() logic
					listPacket.PutLobbyCharacterListEntry(c);

					// Add separator byte between characters, but not after the last one
					if (idx < characters.Count - 1)
						listPacket.PutByte((byte)(idx + 1)); // Separator index
					idx++;
				}

				conn.Send(listPacket);
			}
		}

		/// <summary>
		/// Sends the character list (Login Result 0x50).
		/// Used during direct Login, not Reconnect.
		/// </summary>
		public static void CharacterList(LobbyConnection conn, List<CharacterSummary> characters)
		{
			var packet = new Packet(Op.LoginResponse); // 0x50

			packet.PutByte(1); // Header/Count flag?
			packet.PutByte((byte)characters.Count);

			foreach (var c in characters)
			{
				packet.PutByte(c.ServerId);
				packet.PutByte((byte)c.Name.Length);
				packet.PutString(c.Name, false); // Raw ASCII/Latin1 usually
				packet.PutByte(0x00); // Terminator
				packet.PutInt(c.Level); // Unk1
				packet.PutInt(c.Time1);
				packet.PutInt(c.Time2);
				packet.PutByte(c.CharacterCount);
			}

			conn.Send(packet);
		}

		public static void WorldRedirect(LobbyConnection conn, string ip, int port)
		{
			var packet = new Packet(Op.ServerRedirect);
			byte[] ipBytes = IPAddress.Parse(ip).GetAddressBytes();
			Array.Reverse(ipBytes);
			packet.PutBytes(ipBytes);
			packet.PutUShort((ushort)port);
			packet.PutByte(2);
			packet.PutUInt(conn.Seed);
			packet.PutString(conn.Username, false);
			packet.PutByte(0x27); // Unk
			packet.PutByte(0); // Flag2
			conn.Send(packet);
		}

